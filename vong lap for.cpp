// vong lap for 
#include<stdio.h>

main(){
	int i;
	for(i=0;i<10;i++){
		printf("\n so thu tu la : %d", i+1);
	}
	for(i=0;i<10;i+=2){
		printf("\n so thu tu la : %d", i+1);
	}
	for(i=10;i>=1;i--){
		printf("\n so thu tu la : %d", i);
	}
	for(i=10;i>=1;i-=2){
		printf("\n so thu tu la : %d", i);
	}
}

